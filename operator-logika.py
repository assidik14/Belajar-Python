print("Operator logika AND") # Akan True jika kedua nilai True
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print("--------------------")
print("Operator logika OR") # Akan Trus jika salah satu atau kedua-nya True
print(True or True)
print(True or False)
print(False or True)
print(False or False)
print("--------------------")
print("Operator logika XOR") # Akan True jika salah satu-nya True
print(True ^ True)
print(True ^ False)
print(False ^ True)
print(False ^ False)