print("===== FOR LOOP =====")
# FOR LOOP
for i in range(5):
    print("Hello World!")

print("==== WHILE LOOP ====")
# WHILE LOOP
i = 0
while i < 10:
    print("Increment looping")
    i = i + 1 # Increment

print("===================")

i = 10
while i > 0:
    print("Decrement looping")
    i = i - 1 # Decrement
